import cv2
import numpy as np

# === 1. 创建画布 (矩阵) ===
# 我们创建一个 300(高) x 300(宽) 的矩阵
# 3 代表三个通道：蓝(B)、绿(G)、红(R)
# np.uint8 代表数字范围是 0-255 (计算机里 0是全黑，255是全亮)
img = np.zeros((300, 300, 3), dtype=np.uint8)

print("--- 图片出生了 ---")
print(f"图片形状(Shape): {img.shape}") 
print("这意味着它是一个 300行、300列、3层叠在一起的立体数字块。")

# === 2. 像手术刀一样修改像素 ===
# 计算机眼里的“画画”，其实就是给数组赋值

# 把左上角 (0-100行, 0-100列) 的区域变成【蓝色】
# OpenCV 的颜色顺序是 B-G-R (蓝-绿-红)
# [255, 0, 0] 意味着：蓝色开满(255)，绿色关(0)，红色关(0)
img[0:100, 0:100] = [255, 0, 0] 

# 把右下角 (200-300行, 200-300列) 变成【红色】
img[200:300, 200:300] = [0, 0, 255]

# 把中间 (100-200行) 变成【白色】(三原色全开就是白)
img[100:200, 100:200] = [255, 255, 255]

# === 3. 展示图片 ===
# === 制作“底片”效果 ===
# 用 255 减去当前的颜色。
# 如果原来是 0 (黑)，变成 255 (白)。
# 如果原来是 255 (蓝)，变成 0 (黄色，因为蓝的补色是黄)。
img = 255 - img
cv2.imshow("My First Matrix Image", img)

print("\n按任意键关闭窗口...")
# 等待用户按键，0表示无限等待
cv2.waitKey(0)
cv2.destroyAllWindows()